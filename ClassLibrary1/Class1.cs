using System;
using System.Collections.Generic;
using System.Text;

namespace ClassLibrary1
{
    public class Class1
    {
        public static double dientich(double a)
        {
            return 4*Math.PI*a*a;
        }
        public static double chuvi(double a)
        {
            return a * 2*Math.PI;
        }
        public static double thetich(double a)
        {
            return (Math.PI*Math.Pow(a,3)*4/3);
        }
    }
